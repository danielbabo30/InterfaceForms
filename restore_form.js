  process.stdout.write('Connecting to MongoDB...\n');
  await mongoose.connect(MONGO_URI, {
    serverSelectionTimeoutMS: 15000,
  });
  process.stdout.write('Connected.\n');

  // Fetch existing form
  const form = await Form.findOne({ id: 'e566c32e' });
  if (!form) {
    process.stdout.write('ERROR: Form e566c32e not found in database!\n');
    process.exit(1);
  }
  process.stdout.write(`Found form: "${form.title}" with ${form.questions.length} questions\n`);

  // Build corrected questions
  const questions = buildQuestions();
  const questionCount = questions.filter(q => q.type !== 'PAGE_BREAK').length;

  // Verify Hebrew encoding
  process.stdout.write(`Sample labels check:\n`);
  process.stdout.write(`  [0] ${questions[0].label}\n`);
  process.stdout.write(`  [1] ${questions[1].label}\n`);
  process.stdout.write(`  [2] ${questions[2].label}\n`);

  // Update the form
  form.title = 'שאלון פרטים אישיים';
  form.description = 'שאלון 1-3 — פרטי לווים, נתוני הנכס והעסקה';
  form.questions = questions;
  form.updatedAt = new Date().toISOString();

  // Mark questions as modified (mongoose mixed array)
  form.markModified('questions');
  form.markModified('title');
  form.markModified('description');

  await form.save();
  process.stdout.write(`\nSaved successfully!\n`);

  // Verify by re-fetching
  const saved = await Form.findOne({ id: 'e566c32e' });
  process.stdout.write(`\nVerification — re-fetched form:\n`);
  process.stdout.write(`  title: ${saved.title}\n`);
  process.stdout.write(`  questions count: ${saved.questions.length}\n`);
