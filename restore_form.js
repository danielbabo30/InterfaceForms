function buildQuestions() {
  const questions = [];
  for (const [type, id, required, optionsCsv] of FORM_STRUCTURE) {
    if (type === 'PAGE_BREAK') {
      // id here is the borrower group, optionsCsv is null, required is the title
      questions.push({
        type: 'PAGE_BREAK',
        id: id || '',
        label: required, // PAGE_BREAK rows: col[2] is the title
        required: false,
        options: [],
      });
    } else {
      const label = LABELS[id] || id;
      const options = optionsCsv ? optionsCsv.split(',') : [];
      questions.push({
        type,
        id,
        label,
        required: required === '1',
        options,
      });
    }
  }
  return questions;
}

